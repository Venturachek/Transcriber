def analyze_call(text: str):
    prompt = PROMPT.format(text=text)
    response = requests.post(
        settings.ollama_url,
        json={
            "model": settings.OLLAMA_MODEL,
            "prompt": prompt,
            "stream": False,
        },
        timeout=120
    )
    print("status:", response.status_code)

    res = response.json()["response"].strip()
    if res.startswith("```"):
        res = res.split("```")[1]
        if res.startswith("json"):
            res = res[4:]
    result =  json.loads(res.strip())
    valid = Services.values()
    result["top_100_services"] = [s for s in result.get("top_100_services", []) if s in valid]
    return result
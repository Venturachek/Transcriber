from faster_whisper import WhisperModel
from pathlib import Path
import requests
from src.config import settings

model = WhisperModel("large-v3", device="cpu", compute_type="int8")

def transcribe_audio(audio_path: Path):
    parts, _ = model.transcribe(str(audio_path), language="uk", beam_size=5)
    return " ".join([part.text for part in parts])


def format_text(text: str) -> str:
    prompt = f"""Ти — редактор транскрипцій телефонних дзвінків. Твоє завдання — відформатувати сирий ASR-текст у читабельний діалог.

    ## ФОРМАТ ВИВОДУ
    Кожна репліка з нового рядка у форматі:
    Менеджер: [текст репліки]
    Клієнт: [текст репліки]

    ## ВИЗНАЧЕННЯ СПІКЕРІВ
    - Менеджер — той, хто першим вітається, називає компанію/себе, пропонує послуги
    - Клієнт — той, хто телефонує із запитом або проблемою
    - Якщо спікера неможливо визначити — використай "Невідомо:"

    ## ЩО МОЖНА ВИПРАВЛЯТИ
    - Розділові знаки та великі літери
    - Очевидні ASR-помилки (слова, яких не існує в українській/російській мові)
    - Форму слова (відмінок, число, час) якщо вона явно неправильна за граматикою
    - Суржик або спотворені слова — заміняй на найімовірніший варіант за контекстом

    ## ЩО ЗАБОРОНЕНО
    - Додавати слова або факти, яких не було в оригіналі
    - Змінювати зміст або сенс висловлювання
    - Вигадувати імена, цифри, назви, якщо вони нечіткі
    - Об'єднувати репліки різних спікерів в одну

    ## НЕРОЗБІРЛИВІ МІСЦЯ
    - Якщо слово неможливо відновити за контекстом — постав [нерозбірливо]
    - Якщо ціла фраза незрозуміла — постав [нерозбірливо]
    - Не вгадуй цифри (суми, номери, дати) — краще [нерозбірливо]

    ## ТЕКСТ ДЛЯ ФОРМАТУВАННЯ
    {text}

    Поверни ТІЛЬКИ відформатований діалог. Жодних пояснень, коментарів, заголовків."""

    response = requests.post(
        settings.ollama_url,
        json={
            "model": settings.OLLAMA_MODEL,
            "prompt": prompt,
            "stream": False,
        },
        timeout=300
    )

    return response.json()["response"].strip()